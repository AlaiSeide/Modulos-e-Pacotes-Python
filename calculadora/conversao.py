"""
Resumo:
Celsius para Fahrenheit:
Usa a fórmula (Celsius * 9/5) + 32 para converter a temperatura.

Fahrenheit para Celsius:
Usa a fórmula (Fahrenheit - 32) * 5/9 para converter a temperatura.

Essas fórmulas são como receitas que nos ajudam a mudar a temperatura de uma escala para outra, e com Python, você pode fazer isso de forma rápida e fácil!
"""


def celsius_para_fahrenheit(celsius):
    return (celsius * 9/5) + 32


def fahrenheit_para_celsius(fahrenheit):
    return (fahrenheit - 32) * 5/9
